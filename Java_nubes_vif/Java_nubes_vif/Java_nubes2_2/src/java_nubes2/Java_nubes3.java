    private void txtBeliFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtBeliFocusLost
        // TODO add your handling code here:
        
        int harga = Integer.parseInt(txtHarga.getText());
        int beli = Integer.parseInt(txtBeli.getText());

        int result = harga * beli;

        txtTobar.setText(String.valueOf(result));
 
    }//GEN-LAST:event_txtBeliFocusLost

    private void txtBayarFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtBayarFocusLost
        // TODO add your handling code here:
        
        int tobar = Integer.parseInt(txtTobar.getText());
        int bayar = Integer.parseInt(txtBayar.getText());

        int kembali = bayar - tobar;

        txtKembali.setText(String.valueOf(kembali));
    }//GEN-LAST:event_txtBayarFocusLost

