    private void cbJenisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbJenisActionPerformed
        // TODO add your handling code here:
// Di dalam actionPerformed atau method lain yang diperlukan pada Java_nubes3
    String destination = cbJurusan.getSelectedItem().toString();
    String classType = cbJenis.getSelectedItem().toString();

    TravelTicket ticket;

    if ("Pati".equals(destination)) {
        ticket = new PatiTicket(classType);
    } else if ("Bali".equals(destination)) {
        ticket = new BaliTicket(classType);
    } else if ("Pemalang".equals(destination)) {
        ticket = new PemalangTicket(classType);
    } else {
        // Penanganan jika destinasi tidak valid
        ticket = null;
    }

    if (ticket != null) {
        txtHarga.setText(String.valueOf(ticket.getPrice()));
        txtCetak.setText(ticket.ticketInfo());
    } else {
        txtHarga.setText("");
        txtCetak.setText("Destinasi tidak valid");
    }

     
    }//GEN-LAST:event_cbJenisActionPerformed

